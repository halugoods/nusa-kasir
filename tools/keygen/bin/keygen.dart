// tools/keygen/bin/keygen.dart
//
// Seller-side activation key generator for NUSA Kasir.
//
// Produces keys in EXACTLY the same format the app verifies
// (see lib/core/activation/activation_key.dart). The signing + format logic
// is re-implemented inline here so the tool does NOT depend on the app package.
//
// Format:
//   NUSA-<serial4>-<serial4>-<sigB32 groups of 4...>
//   serial  = 8 chars from alphabet ABCDEFGHJKLMNPQRSTUVWXYZ23456789 (8 random bytes)
//   signature = Ed25519(serial.codeUnits) -> 64 bytes
//   base32-encode signature with alphabet ABCDEFGHIJKLMNOPQRSTUVWXYZ234567
//   split serial into 4-char groups, then signature base32 into 4-char groups,
//   join with '-', prefix 'NUSA-'.

import 'dart:io';
import 'dart:math';

import 'package:args/args.dart';
import 'package:cryptography/cryptography.dart';
import 'package:supabase/supabase.dart';

const String _prefix = 'NUSA-';
const int _serialLen = 8;
const String _serialAlphabet = 'ABCDEFGHJKLMNPQRSTUVWXYZ23456789';
const String _b32Alphabet = 'ABCDEFGHIJKLMNOPQRSTUVWXYZ234567';

const String _privateKeyFile = 'nusa_key.private';
const String _publicKeyPath =
    'C:/Users/ertyui/ZCodeProject/nusa_kasir/lib/core/activation/activation_public_key.dart';

final Ed25519 _alg = Ed25519();
final Random _rng = Random.secure();

String generateSerial() {
  final buf = StringBuffer();
  for (var i = 0; i < _serialLen; i++) {
    buf.write(_serialAlphabet[_rng.nextInt(_serialAlphabet.length)]);
  }
  return buf.toString();
}

String base32Encode(List<int> data) {
  final out = StringBuffer();
  for (var i = 0; i < data.length; i += 5) {
    final chunk = data.sublist(i, i + 5 > data.length ? data.length : i + 5);
    var bits = 0, value = 0;
    for (final b in chunk) {
      value = (value << 8) | b;
      bits += 8;
    }
    while (bits >= 5) {
      bits -= 5;
      out.write(_b32Alphabet[(value >> bits) & 31]);
    }
    if (bits > 0) {
      out.write(_b32Alphabet[(value << (5 - bits)) & 31]);
    }
  }
  return out.toString();
}

String format(String serial, List<int> signature) {
  final sigB32 = base32Encode(signature);
  final groups = <String>[];
  var i = 0;
  while (i < serial.length) {
    groups.add(serial.substring(i, i + 4 > serial.length ? serial.length : i + 4));
    i += 4;
  }
  i = 0;
  while (i < sigB32.length) {
    groups.add(sigB32.substring(i, i + 4 > sigB32.length ? sigB32.length : i + 4));
    i += 4;
  }
  return '$_prefix${groups.join('-')}';
}

Future<SimpleKeyPair> loadOrCreateKeyPair() async {
  final file = File(_privateKeyFile);
  if (await file.exists()) {
    final hex = (await file.readAsString()).trim();
    final bytes = <int>[];
    for (var i = 0; i < hex.length; i += 2) {
      bytes.add(int.parse(hex.substring(i, i + 2), radix: 16));
    }
    return _alg.newKeyPairFromSeed(bytes);
  } else {
    final kp = await _alg.newKeyPair();
    final priv = await kp.extractPrivateKeyBytes();
    await file.writeAsString(priv.map((b) => b.toRadixString(16).padLeft(2, '0')).join());
    return kp;
  }
}

Future<void> writePublicKey(List<int> pubBytes) async {
  final body = pubBytes.join(', ');
  final content = '// GENERATED by tools/keygen — DO NOT EDIT BY HAND.\n'
      'const List<int> nusaActivationPublicKey = <int>[$body];\n';
  await File(_publicKeyPath).writeAsString(content);
}

Future<void> main(List<String> args) async {
  final parser = ArgParser()
    ..addOption('count', abbr: 'c', defaultsTo: '1')
    ..addFlag('skip-db', defaultsTo: false);

  final results = parser.parse(args);
  final count = int.tryParse(results['count'] as String) ?? 1;
  final skipDb = results['skip-db'] as bool;

  final kp = await loadOrCreateKeyPair();
  final publicKey = await kp.extractPublicKey();
  final pubBytes = publicKey.bytes;
  await writePublicKey(pubBytes);

  final keys = <String>[];
  String? lastSerial;
  List<int>? lastSig;

  final insertCreds =
      !skipDb && Platform.environment['SUPABASE_URL'] != null && Platform.environment['SUPABASE_SERVICE_ROLE_KEY'] != null;

  SupabaseClient? client;
  if (insertCreds) {
    client = SupabaseClient(
      Platform.environment['SUPABASE_URL']!,
      Platform.environment['SUPABASE_SERVICE_ROLE_KEY']!,
    );
  }

  for (var n = 0; n < count; n++) {
    final serial = generateSerial();
    final sig = await _alg.sign(serial.codeUnits, keyPair: kp);
    final key = format(serial, sig.bytes);
    keys.add(key);
    lastSerial = serial;
    lastSig = sig.bytes;

    if (client != null) {
      try {
        await client.from('licenses').insert({
          'key': key,
          'serial': serial,
          'status': 'issued',
        });
      } catch (e) {
        stderr.writeln('Warning: Supabase insert failed for $key: $e');
      }
    } else {
      stderr.writeln('Skipping Supabase insert (no creds or --skip-db)');
    }
  }

  for (final k in keys) {
    print(k);
  }

  // Self-check: re-verify one generated key with the public key.
  if (lastSerial != null && lastSig != null) {
    final selfSig = Signature(lastSig, publicKey: publicKey);
    final ok = await _alg.verify(lastSerial.codeUnits, signature: selfSig);
    print('SELF-CHECK: verify=$ok');
  }
}
